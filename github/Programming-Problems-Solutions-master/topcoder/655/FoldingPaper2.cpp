const int inf = 100000000;

class FoldingPaper2 {
public:
	int solve(int W, int H, int A) {
		int ans = inf;
		for(int i = 1; i <= A; i++)
        {
            if(A%i) continue;
            ans = min(ans, func(W,i)+func(H,A/i));
            //printf("%d %d %d %d\n", i, A/i, func(W,i), func(H,A/i));
        }
        if(ans == inf) return -1;
        return ans;
	}
    int func(int ase, int bana)
    {
        if(ase<bana) return inf;
        if(ase == bana) return 0;
        if(ase <= bana*2) return 1;
        return func((ase+1)/2, bana)+1;
    }
};



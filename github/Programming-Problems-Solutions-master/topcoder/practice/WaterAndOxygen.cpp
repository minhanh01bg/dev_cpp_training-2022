typedef long long ll;

class WaterAndOxygen {
public:
	double maxDays(int remainH20, int remainO2, int costH2O, int costO2) {
		if((ll) remainH20*costO2 <= (ll) remainO2*costH2O)
        {
            return (double) remainH20/costH2O;
        }
        double ans = (double) remainO2/costO2;
        double baki = remainH20 - costH2O * ans;
        ans += (double) baki/(costH2O+2.0*costO2);
        return ans;
	}
};



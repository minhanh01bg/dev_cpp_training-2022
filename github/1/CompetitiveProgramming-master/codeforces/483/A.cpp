int main() {
    ios :: sync_with_stdio(false); cin.tie(NULL);
    cout << (fixed) << setprecision(6);
    long long l, r;
    while (cin >> l >> r) {
        if (l + 1 >= r) cout << -1 << endl;
        else {
            if (__gcd(l, l+2) > 1) cout << l << ' ' << l+1 << ' ' << l+2 << endl;
            else if (l+3 <= r) cout << l+1 << ' ' << l+2 << ' ' << l+3 << endl;
            else cout << -1 << endl;
        }
    }
    return 0;
}


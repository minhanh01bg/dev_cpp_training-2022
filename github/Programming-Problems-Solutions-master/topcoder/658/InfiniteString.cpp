class InfiniteString {
public:
	string equal(string s, string t) {
		int len1 = s.size();
		int len2 = t.size();
        int lcm = len1*len2/__gcd(len1,len2);
        int a = 0, b = 0;
        for(int i = 0; i < lcm; i++)
        {
            if(a == len1) a = 0;
            if(b == len2) b = 0;
            if(s[a] != t[b])
            {
                return "Not equal";
            }
            a++;
            b++;
        }
        return "Equal";
	}
};



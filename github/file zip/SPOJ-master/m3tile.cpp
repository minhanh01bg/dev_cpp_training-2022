#include <iostream>
using namespace std;
int dp[31][2][2][2];
int main()
{
	dp[0][1][1][1]=1;
	dp[1][0][0][0]=1;
	dp[1][1][1][0]=1;
	dp[1][0][1][1]=1;
	int i;
	for (i=2; i<=30; i++)
	{
		dp[i][0][0][0]=dp[i-1][1][1][1];
		dp[i][0][0][1]=dp[i-1][1][1][0];
		dp[i][0][1][0]=dp[i-1][1][0][1];
		dp[i][0][1][1]=dp[i-1][1][1][1]+dp[i-1][1][0][0];
		dp[i][1][0][0]=dp[i-1][0][1][1];
		dp[i][1][0][1]=dp[i-1][0][1][0];
		dp[i][1][1][0]=dp[i-1][1][1][1]+dp[i-1][0][0][1];
		dp[i][1][1][1]=dp[i-1][0][0][0]+dp[i-1][0][1][1]+dp[i-1][1][1][0];
	}
	for(;;)
	{
		scanf("%d",&i);
		if (!~i) return 0;
		printf("%d\n",dp[i][1][1][1]);
	}
}

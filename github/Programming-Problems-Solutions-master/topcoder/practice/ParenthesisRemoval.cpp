typedef long long ll;

#define MX 2505
#define inf 100000000

const ll mod = 1000000007ll;

ll dp[MX];
string str;
int len;

ll func(int pos, int p)
{
    if(pos == len)
    {
        if(p == 0) return 1;
        return 0;
    }
    ll & res = dp[pos];
    if(res != -1) return res;
    res = 0;
    if(str[pos] == '(')
    {
        res = func(pos+1,p+1);
    }
    else{
        if(p)
            res = func(pos+1,p-1)*p;
    }
    res %= mod;
    return res;
}

class ParenthesisRemoval {
public:
	int countWays(string s) {
        memset(dp,-1,sizeof dp);
        str = s;
        len = str.size();
        return func(0,0);
	}
};



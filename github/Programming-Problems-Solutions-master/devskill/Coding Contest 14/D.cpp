typedef int var;

var rec(var n,var m){
    if(n==0 || m==0) return 0;
    var res = (n | m) & (n | m);
    res += rec( n , m -1 );
    res += rec( n-1, m   );
    res -= rec( n-1, m-1 );
    return res;
}

var solve(var n){
   var ret  = rec(n,n);
   return ret;
}



